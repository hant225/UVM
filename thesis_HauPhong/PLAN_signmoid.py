def sigmoid(x):
    x = abs(x)
    if x >= 5:
        s = 1
    elif x >= 2.375:
        s = 0.03125*abs(x) + 0.84375
    elif x >= 1:
        s = 0.125*abs(x) + 0.625
    else:
        s = 0.25*abs(x) + 0.5
    return s

